import { Geist, Geist_Mono } from "next/font/google";
import "./globals.css";
import { Toaster } from "sonner";
import NextProvider from "@/components/providers/nextProvider";
import LayoutWrapper from "@/components/componentts/LayoutWrapper";
import { UsersProvider } from "@/context/usersContext";
import { AuthProvider } from "@/context/AuthContext";
import { getAllUsers } from "@/services/firebase";

const geistSans = Geist({
  variable: "--font-geist-sans",
  subsets: ["latin"],
});

const geistMono = Geist_Mono({
  variable: "--font-geist-mono",
  subsets: ["latin"],
});

export const metadata = {
  title: "Create Next App",
  description: "Generated by create next app",
};

export default async function RootLayout({ children }) {
  const users = await getAllUsers();
  console.log(users);

  return (
    <html lang="en">
      <body
        className={`${geistSans.variable} ${geistMono.variable} antialiased debug-screens`}
      >
        <Toaster />
        <AuthProvider>
          <NextProvider>
            <UsersProvider initialUsers={users}>
              <LayoutWrapper>{children}</LayoutWrapper>
            </UsersProvider>
          </NextProvider>
        </AuthProvider>
      </body>
    </html>
  );
}
